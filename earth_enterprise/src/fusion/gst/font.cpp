GLuint fontOffset;

void MakeRasterFont(void) {
  GLuint i;
  glPixelStorei(GL_UNPACK_ALIGNMENT, 1);

  fontOffset = glGenLists (128);
  for (i = 32; i < 127; i++) {
    glNewList(i+fontOffset, GL_COMPILE);
    glBitmap(8, 13, 0.0, 2.0, 10.0, 0.0, rasters[i - 32]);
    glEndList();
  }
}

void InitGLFonts(void) {
  glShadeModel(GL_FLAT);
  MakeRasterFont();
}

void PrintGLString(const char* fmt, ...) {
  // assemble string
  static char str[100];
  va_list ap;
  va_start(ap, fmt);
  vsnprintf(str, 100, fmt, ap);
  va_end(ap);

  glPushAttrib(GL_LIST_BIT);
  glListBase(fontOffset);
  glCallLists(strlen(str), GL_UNSIGNED_BYTE, reinterpret_cast<GLubyte*>(str));
  glPopAttrib();
}
